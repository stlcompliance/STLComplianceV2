import { useId, useState, type ReactNode } from 'react'
import type { PrintDocumentStatus } from './types'

function formatDateTime(value: string | null | undefined) {
  if (!value) {
    return 'Not set'
  }

  const parsed = new Date(value)
  if (Number.isNaN(parsed.getTime())) {
    return value
  }

  return parsed.toLocaleString(undefined, {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
    timeZoneName: 'short',
  })
}

function formatDocumentStatus(value: PrintDocumentStatus) {
  switch (value) {
    case 'draft':
      return 'Draft'
    case 'working_copy':
      return 'Working copy'
    case 'official':
      return 'Official'
    case 'copy':
      return 'Copy'
    case 'redacted':
      return 'Redacted'
    default:
      return value
  }
}

function documentStatusClasses(status: PrintDocumentStatus) {
  switch (status) {
    case 'official':
      return 'border-emerald-200 bg-emerald-50 text-emerald-900'
    case 'copy':
      return 'border-sky-200 bg-sky-50 text-sky-900'
    case 'redacted':
      return 'border-amber-200 bg-amber-50 text-amber-900'
    case 'draft':
      return 'border-violet-200 bg-violet-50 text-violet-900'
    default:
      return 'border-slate-200 bg-slate-50 text-slate-900'
  }
}

export function PrintableDocumentShell({
  title,
  subtitle,
  productLabel,
  tenantLabel,
  sourceDisplayRef,
  documentStatus = 'working_copy',
  generatedAt,
  generatedBy,
  watermarkLabel,
  footer,
  signatureSection,
  appendixSection,
  children,
}: {
  title: string
  subtitle?: string
  productLabel?: string
  tenantLabel?: string
  sourceDisplayRef?: string
  documentStatus?: PrintDocumentStatus
  generatedAt?: string
  generatedBy?: string
  watermarkLabel?: string | false
  footer?: ReactNode
  signatureSection?: ReactNode
  appendixSection?: ReactNode
  children: ReactNode
}) {
  const resolvedGeneratedAt = generatedAt ?? new Date().toISOString()
  const statusLabel = formatDocumentStatus(documentStatus)
  const metadataItems = [
    productLabel ? { label: 'Product', value: productLabel } : null,
    tenantLabel ? { label: 'Tenant', value: tenantLabel } : null,
    sourceDisplayRef ? { label: 'Source reference', value: sourceDisplayRef } : null,
    { label: 'Generated', value: formatDateTime(resolvedGeneratedAt) },
    generatedBy ? { label: 'Generated by', value: generatedBy } : null,
  ].filter((item): item is { label: string; value: string } => item !== null)

  return (
    <section
      className="stl-print-document relative mx-auto max-w-5xl bg-white px-6 py-8 text-slate-900 shadow-sm"
      data-print-document
      data-print-surface
    >
      {watermarkLabel ? <DraftWatermark label={watermarkLabel} /> : null}
      <div className="relative space-y-6">
        <header className="border-b border-slate-200 pb-5" data-print-section="document-header">
          <div className="flex flex-wrap items-start justify-between gap-4">
            <div className="min-w-0">
              {productLabel || tenantLabel ? (
                <p className="text-xs font-semibold uppercase tracking-[0.18em] text-slate-500">
                  {[productLabel, tenantLabel].filter(Boolean).join(' · ')}
                </p>
              ) : null}
              <h1 className="mt-2 break-words text-3xl font-semibold text-slate-950">{title}</h1>
              {subtitle ? <p className="mt-2 max-w-4xl text-sm leading-6 text-slate-600">{subtitle}</p> : null}
            </div>
            <div className="shrink-0 text-right">
              <span
                className={`inline-flex items-center rounded-full border px-3 py-1 text-xs font-semibold uppercase tracking-wide ${documentStatusClasses(documentStatus)}`}
              >
                {statusLabel}
              </span>
              {sourceDisplayRef ? (
                <p className="mt-3 text-xs font-medium uppercase tracking-wide text-slate-500">
                  Source reference
                  <br />
                  <span className="text-sm font-semibold normal-case tracking-normal text-slate-900">
                    {sourceDisplayRef}
                  </span>
                </p>
              ) : null}
            </div>
          </div>

          {metadataItems.length > 0 ? (
            <dl
              className="mt-5 grid gap-3 md:grid-cols-2 xl:grid-cols-4"
              data-print-section="document-metadata"
            >
              {metadataItems.map((item) => (
                <div key={item.label} className="rounded-lg border border-slate-200 bg-slate-50 p-3">
                  <dt className="text-[11px] font-semibold uppercase tracking-wide text-slate-500">
                    {item.label}
                  </dt>
                  <dd className="mt-2 break-words text-sm font-medium text-slate-950">{item.value}</dd>
                </div>
              ))}
            </dl>
          ) : null}
        </header>

        <div className="space-y-6" data-print-section="document-body">
          {children}
        </div>

        {signatureSection ? (
          <section
            className="break-before-page space-y-3 border-t border-slate-200 pt-5"
            data-print-section="signature-section"
            data-print-break-before="page"
          >
            <h2 className="text-sm font-semibold uppercase tracking-wide text-slate-500">
              Signature / approval
            </h2>
            <div className="rounded-lg border border-slate-200 bg-white p-4">{signatureSection}</div>
          </section>
        ) : null}

        {appendixSection ? (
          <section
            className="break-before-page space-y-3 border-t border-slate-200 pt-5"
            data-print-section="appendix-section"
            data-print-break-before="page"
          >
            <h2 className="text-sm font-semibold uppercase tracking-wide text-slate-500">
              Appendix / attachments
            </h2>
            <div className="rounded-lg border border-slate-200 bg-white p-4">{appendixSection}</div>
          </section>
        ) : null}

        {footer ? (
          <footer className="border-t border-slate-200 pt-4 text-sm text-slate-600" data-print-section="document-footer">
            {footer}
          </footer>
        ) : null}
      </div>
    </section>
  )
}

